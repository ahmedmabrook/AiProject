public class Build {
    private int priceBuild;
    private int foodUseBuild;

    private int energyUseBuild;
    private int materialsUseBuild;
    private int prosperityBuild;

    public Build(int priceBuild , int foodUseBuild, int energyUseBuild , int materialsUseBuild , int prosperityBuild)
    {
        this.priceBuild = priceBuild ;
        this.foodUseBuild = foodUseBuild;
        this.energyUseBuild = energyUseBuild;
        this.materialsUseBuild = materialsUseBuild;
        this.prosperityBuild = prosperityBuild;
    }
}
